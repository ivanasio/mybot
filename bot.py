# Главное меню
def main_menu():
    keyboard = [
        [InlineKeyboardButton("🚀 Начать экскурсию", callback_data="start_tour")]
    ]
    return InlineKeyboardMarkup(keyboard)

# Клавиатура экскурсии
def get_keyboard(index):
    buttons = []

    if index > 0:
        buttons.append(
            InlineKeyboardButton("⬅️ Назад", callback_data=f'section_{index - 1}')
        )

    if index < len(sections) - 1:
        buttons.append(
            InlineKeyboardButton("Далее ➡️", callback_data=f'section_{index + 1}')
        )

    keyboard = [buttons]

    keyboard.append(
        [InlineKeyboardButton("🏠 В меню", callback_data="menu")]
    )

    return InlineKeyboardMarkup(keyboard)

# Команда /start
async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Добро пожаловать в музей школы имени В.М. Комарова!\n\nВыберите действие:",
        reply_markup=main_menu()
    )

# Обработка кнопок
async def button(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()

    data = query.data

    if data == "start_tour":
        await query.edit_message_text(
            text=sections[0],
            reply_markup=get_keyboard(0)
        )
        return

    if data == "menu":
        await query.edit_message_text(
            text="Главное меню:\n\nВыберите действие:",
            reply_markup=main_menu()
        )
        return

    if data.startswith("section_"):
        index = int(data.split("_")[1])
        await query.edit_message_text(
            text=sections[index],
            reply_markup=get_keyboard(index)
        )

